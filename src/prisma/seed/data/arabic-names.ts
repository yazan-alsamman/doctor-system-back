export function composeArabicName(rng: { pick: <T>(a: readonly T[]) => T }, sex: 'male' | 'female') {
  const given = sex === 'female' ? rng.pick(GIVEN_FEMALE) : rng.pick(GIVEN_MALE);
  const family = rng.pick(FAMILY_NAMES);
  return `${given} ${family}`;
}
